/// Font families from the design. `display` (Space Grotesk) carries amounts,
/// titles and anything numeric — it ships tabular figures. `body` (Plus Jakarta
/// Sans) carries prose. Declared as assets in pubspec; never referenced by raw
/// string in widgets.
abstract final class AppFonts {
  static const String display = 'Space Grotesk';
  static const String body = 'Plus Jakarta Sans';
}
